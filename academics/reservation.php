      <h3 class="text-dark">Medium of Instruction</h3>
      <p>The medium of instruction will be in both Hindi and English. The examination may be written in either Hindi or English, except for the language subjects.</p>
      <p>It is essential for students to achieve fluency in both areas of language, i.e., grapheme and phoneme. A well-equipped language lab is installed and handled by qualified faculty for the benefit of the students. Spoken English classes are held regularly to enable students to be absorbed in reputed English medium institutions in India and abroad.</p>
