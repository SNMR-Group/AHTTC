        <li>Candidates with a degree in Science (other than Mathematics) can offer Mathematics as a method subject.</li>
        <li>Candidates offering Civics as a method subject should have studied one of the following: History, Geography, Political Science, Economics, or Commerce at the Degree level.</li>
